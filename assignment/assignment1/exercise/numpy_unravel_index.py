# -*- coding: utf-8 -*-

import numpy as np
np.unravel_index([22, 41, 37], (7,6))
np.unravel_index([31, 41, 13], (7,6), order='F')
np.unravel_index(1621, (6,7,8,9))


######################################################################

# =============================================================================
# indices参数是一个向量，通过该向量中值求出对应的下标；矩阵(7,6)
# 第22个元素是 3*6+4，因此：(array([3,  ], dtype=int64), array([4,  ], dtype=int64))
# 第41个元素是 6*6+5，因此：(array([3,6,  ], dtype=int64), array([4, 5,  ], dtype=int64))
# 第37个元素是 6*6+1，因此：(array([3,6, 6 ], dtype=int64), array([4, 5, 1 ], dtype=int64))
# 
# =============================================================================
# =============================================================================
# np.unravel_index([22, 41, 37], (7,6))
# Out[21]: (array([3, 6, 6], dtype=int64), array([4, 5, 1], dtype=int64))
# =============================================================================






######################################################################

# indices参数是一个向量，矩阵(7,6)  index的横竖索引 第二个array放到前面计算
# 第31个元素是 4*7+3，因此：(array([3,  ], dtype=int64), array([4,  ], dtype=int64))
# 第41个元素是 5*7+6，因此：(array([3,6,  ], dtype=int64), array([4, 5,  ], dtype=int64))
# 第13个元素是 1*7+6，因此：(array([3,6, 6 ], dtype=int64), array([4, 5, 1 ], dtype=int64))

# =============================================================================
# np.unravel_index([31, 41, 13], (7,6), order='F')
# Out[22]: (array([3, 6, 6], dtype=int64), array([4, 5, 1], dtype=int64))
# 
# =============================================================================




######################################################################
# =============================================================================
# indices参数是一个标量:1621，6*7*8*9的矩阵；那么返回的是一个向量，维数=矩阵的维数，向量的值其实就是在矩阵中对应的下标。
# 1621/(7*8*9)=3，
# (1621-3*7*8*9)/(8*9)=1，
# (1621-3*7*8*9-1*8*9)/9=4，
# (1621-3*7*8*9-1*8*9-4*9)=1。
# 返回的向量为array(3,1,4,1)
# =============================================================================
